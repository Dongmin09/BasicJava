package Chapter02.study1;

public class VarEx1 {

	public static void main(String[] args) {

		int year = 0;
		int age = 14;

		System.out.println(year);
		System.out.println(age);

		
		year = age + 2000;   //age 값에 2000을 더해서 저장
		age = age+1;  // age 저장된 값 + 1 증가
		
		System.out.println(year);
		System.out.println(age);
		
		

		
	}

}
