public class Circle {

	private double radius;
	private double x;
	private double y;

	
	
	// 생성자
	public Circle() {
	}
	
	
	double getArea() {
		return 0;
	}
	
	public double getRadius() {
		return radius;
	}
	public void setRadius(double radius) {
		if(radius >0) {
		this.radius = radius;
		}
	}
		
	public double getX() {
		return x;
	}
	public void setX(double X) {
		this.x = x;
	}
	public double getY() {
		return x;
	}
	public void setY(double Y) {
		this.y = y;
	}
	
	
}
